<template>
<div class="tab-bar-item" @click="itemClick">
  <div v-if="!isActive"><slot name="item-icon"></slot></div>
  <div v-else><slot name="item-icon-active"></slot></div>
  <div :style="[activeColor]"><slot name="item-text"></slot></div>
</div>
</template>

<script>
export default {
  name: "TabBarItem",
  props: {
    url: String,
    colorStyle: {
      type: String,
      default: 'red'
    }
  },
  data() {
    return {
      //isActive: true
    }
  },
  computed: {
    isActive() {
      return this.$route.path.indexOf(this.url) !== -1
    },
    activeColor() {
      return this.isActive? {color: this.colorStyle} : {}
    }
  },
  methods: {
    itemClick() {
      this.$router.replace(this.url).catch(err=>err)
    }
  }
}
</script>

